
class Fruit {
    private int type;
    private int sweetness;
    private int color;

    public Fruit(int type, int sweetness, int color) {
        this.type = type;
        this.sweetness = sweetness;
        this.color = color;
    }

    public int getName() {
        return type;
    }

    public int getSweetness() {
        return sweetness;
    }

    public int getColor() {
        return color;
    }
}

    

 



